    private void cargarDemo(String pAlg)
    {
        int nBloques = Integer.parseInt(this.jTextField2.getText());
        int iComplexity = Integer.parseInt(this.jTextField1.getText());
        this.bc= new BlockChain(iComplexity,"0",pAlg);
        //SHA3-256, SHA3-512, MD5, SHA-256, SHA-512
        this.bc.createGenesis();
        this.iteraciones=this.bc.getBlock(0).getNonce();
        for(int i=0; i<nBloques; i++)
        {
            bc.createBlock();//crea un bloque propuesto
            bc.getLastBlock().setTransaction("Kevin Sigfredo Hernandez Hernandez",160.81,"Genesis Rubi Brito Mancia");
            this.iteraciones+=bc.mineBlock();
        }
        
        iComplexity = 0;//borrar lÃ­nea
    }
    
