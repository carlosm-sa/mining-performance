    public boolean validarPOW()
    {
        boolean pow=true;
        for(int i=0; i<this.bc.size(); i++)
        {
            pow=this.bc.getProofOfWork_overBlock(
               this.bc.getBlock(i));
            if(!pow) return false;
        }
        return true;
    }
    
